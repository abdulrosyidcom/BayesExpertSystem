    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $dataset = new Dataset();
        $dataset->test = $request->test;
        $dataset->save;
        $dataset->diseases()->attach($request->diseases);
        $dataset->symptoms()->attach($request->symptoms);

        return redirect('admin/dataset')->with('toast_success', 'Dataset Berhasil Ditambahkan');
    }

